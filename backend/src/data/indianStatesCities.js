// Get all states
export const getAllStates = () => {
  return Object.keys(indianStatesCities).sort();
};

// Get cities for a state
export const getCitiesByState = (state) => {
  if (!state) return [];
  const normalizedState = state.trim();
  return indianStatesCities[normalizedState] || [];
};

// Check if state is valid
export const isValidState = (state) => {
  if (!state) return false;
  return Object.keys(indianStatesCities).includes(state.trim());
};

// Check if city is valid for a state
export const isValidCity = (city, state) => {
  if (!city || !state) return false;
  const cities = getCitiesByState(state);
  return cities.some(c => c.toLowerCase() === city.trim().toLowerCase());
};

// Get all cities (flattened)
export const getAllCities = () => {
  const allCities = [];
  Object.values(indianStatesCities).forEach(cities => {
    allCities.push(...cities);
  });
  return [...new Set(allCities)].sort();
};

// Search cities by name (case-insensitive)
export const searchCities = (query) => {
  if (!query) return [];
  const searchTerm = query.toLowerCase().trim();
  const results = [];
  
  Object.entries(indianStatesCities).forEach(([state, cities]) => {
    cities.forEach(city => {
      if (city.toLowerCase().includes(searchTerm)) {
        results.push({ city, state });
      }
    });
  });
  
  return results;
};







